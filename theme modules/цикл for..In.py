# оператор цикла for.. in так же являются опертаорм цикла для последовательности. Это Упорядочность набор элементов
for i in list(range(300, 350)):  # Range в Python — это встроенная функция, которая возвращает итерируемый объект,
    # содержащий целые числа. Показывает последовательность в коде от начало до конца по указанному циклу,
    # то есть интервал.
    print(i)
else:
    print('Цикл for завершен')
